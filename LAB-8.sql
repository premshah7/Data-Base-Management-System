--Part – A:


--1.Display Total Sales Amount by Region.
SELECT SUM(SALES_AMOUNT) AS TOTAL_SALES_AMOUNT_BY_REGION FROM SALES_DATA GROUP BY REGION;

--2.Display Average Sales Amount by Product
SELECT AVG(SALES_AMOUNT) AS AVERAGE_SALES_AMOUNT_BY_PRODUCT FROM SALES_DATA GROUP BY PRODUCT;
 
--3.Display Maximum Sales Amount by Year
SELECT MAX(SALES_AMOUNT) AS MAXIMUN_SALES_AMOUNT_BY_YEAR FROM SALES_DATA GROUP BY YEAR_;

--4.Display Minimum Sales Amount by Region and Year
SELECT MIN(SALES_AMOUNT) AS MINIMUM_SALES_AMOUNT_BY_YEAR_BY_REGION FROM SALES_DATA GROUP BY YEAR_ , REGION;

--5.Count of Products Sold by Region
SELECT COUNT(PRODUCT) AS COUNT_OF_PRODUCT_SOLD_BY_REGION FROM SALES_DATA GROUP BY REGION;

--6.Display Sales Amount by Year and Product
SELECT SUM(SALES_AMOUNT) AS SALES_AMOUNT_BY_YEAR_AND_PRODUCT FROM SALES_DATA GROUP BY YEAR_ , PRODUCT;

--7.Display Regions with Total Sales Greater Than 5000
SELECT REGION , SUM(SALES_AMOUNT) FROM SALES_DATA GROUP BY REGION HAVING SUM(SALES_AMOUNT)>5000;

--8.Display Products with Average Sales Less Than 10000
SELECT PRODUCT , AVG(SALES_AMOUNT) FROM SALES_DATA GROUP BY PRODUCT HAVING AVG(SALES_AMOUNT)<10000;

--9.Display Years with Maximum Sales Exceeding 500
SELECT YEAR_ , MAX(SALES_AMOUNT) FROM SALES_DATA GROUP BY YEAR_ HAVING MAX(SALES_AMOUNT)>500;

--10.Display Regions with at Least 3 Distinct Products Sold.
SELECT DISTINCT PRODUCT , COUNT(PRODUCT) FROM SALES_DATA GROUP BY PRODUCT HAVING COUNT(PRODUCT)>3;

--11.Display Years with Minimum Sales Less Than 1000
SELECT YEAR_ , COUNT(YEAR_) FROM SALES_DATA GROUP BY YEAR_ HAVING MIN(SALES_AMOUNT)>1000;

--12.Display Total Sales Amount by Region for Year 2023, Sorted by Total Amount
SELECT SUM(SALES_AMOUNT) AS TOTAL_AMOUNT_BY_REGION  FROM SALES_DATA  WHERE YEAR_ = '2023' GROUP BY REGION ORDER BY SUM(SALES_AMOUNT);  

