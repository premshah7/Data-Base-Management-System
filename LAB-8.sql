--1.Display Products with Average Sales Amount Between 1000 and 2000, Ordered by Product Name
SELECT PRODUCT , AVG(SALES_AMOUNT) AS AVRAGE_SALES_AMOUNT FROM SALES_DATA WHERE SALES_AMOUNT BETWEEN 1000 AND 2000 GROUP BY PRODUCT ORDER BY PRODUCT;

--2.Display Years with More Than 5 Orders from Each Region
SELECT REGION , YEAR_  FROM SALES_DATA GROUP BY YEAR_ , REGION HAVING COUNT(YEAR_) > 5;

--3.Display Regions with Average Sales Amount Above 1500 in Year 2023 sort by amount in descending.
SELECT REGION , AVG(SALES_AMOUNT) AS AVERAGE_SALES_AMOUNT FROM SALES_DATA WHERE YEAR_ = '2023' GROUP BY REGION , SALES_AMOUNT HAVING AVG(SALES_AMOUNT) > 1500 ORDER BY SALES_AMOUNT DESC; 

