--1.Display Count of Orders by Year and Region, Sorted by Year and Region
SELECT COUNT(YEAR_) AS COUNT_OF_YEAR , COUNT(REGION) AS COUNT_OF_REGION FROM SALES_DATA ORDER BY COUNT(YEAR_) , COUNT(REGION);

--2.Display Regions with Maximum Sales Amount Exceeding 1000 in Any Year, Sorted by Region
SELECT REGION , MAX(SALES_AMOUNT) FROM SALES_DATA GROUP BY REGION HAVING MAX(SALES_AMOUNT) > 1000 ORDER BY REGION;

--3.Display Years with Total Sales Amount Less Than 1000, Sorted by Year Descending
SELECT YEAR_ , SUM(SALES_AMOUNT) FROM SALES_DATA GROUP BY YEAR_ HAVING SUM(SALES_AMOUNT) < 1000 ORDER BY YEAR_ DESC;

--4.Display Top 3 Regions by Total Sales Amount in Year 2024
SELECT TOP 3 REGION , SUM(SALES_AMOUNT) FROM SALES_DATA WHERE YEAR_ = '2024' GROUP BY SALES_AMOUNT , REGION ;

